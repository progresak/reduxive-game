import {getRandomMapTile} from "../../../features/map/MapTile";


export const generateDynamicMap = (width, height) => {
    const map = [];
    for (let y = 0; y < height; y++) {
        map[y] = [];
        for (let x = 0; x < width; x++) {
            map[y][x] = getRandomMapTile()
        }
    }
    return map;
};

